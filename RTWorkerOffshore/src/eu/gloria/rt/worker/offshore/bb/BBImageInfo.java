package eu.gloria.rt.worker.offshore.bb;

public class BBImageInfo {
	
	public String opId;
	public String imgId;
	public String fileName;
	public String urlFits;
	public String urlJpg;
	
	public String getOpId() {
		return opId;
	}
	public void setOpId(String opId) {
		this.opId = opId;
	}
	public String getImgId() {
		return imgId;
	}
	public void setImgId(String imgId) {
		this.imgId = imgId;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getUrlFits() {
		return urlFits;
	}
	public void setUrlFits(String urlFits) {
		this.urlFits = urlFits;
	}
	public String getUrlJpg() {
		return urlJpg;
	}
	public void setUrlJpg(String urlJpg) {
		this.urlJpg = urlJpg;
	}

}
